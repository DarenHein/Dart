// String en dart 
/**
 * son cadenas de caracteres 
 * se pueden acceder a cada elemento de la cadena como si fuera una lista o array 
 * tiene un chingo de metodos pero esos mas adelante 
 */

void main (){
  String cadena =  "Hola mundo "; 
  // imprimir una cadena 
  print(cadena); 
  // sumar dos cadenas 
  String cadena2 = "Luis"; 
  print(cadena + cadena2); // ocuapndo el simbolo de concatenacion 
  // accediendo a un solo caracter de la cadena 
  String caracter = cadena[0]; 
  print(caracter); // imprime la H de la cadena hola mundo 
  
}