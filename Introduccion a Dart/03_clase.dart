
// case 
// son cassos caso 1 ,2 ,3 y si no existe el caso entra en el default 
void main(){

  var numero = 3 ; 

  switch(numero){
    case 1 : 
    print("opcion 1"); 
    break; 
    case 2 : 
    print("opcion 2 "); 
    break; 
    case 3 : 
    print("opcion 3"); 
    break; 
    default : 
    print("opcion invalida"); 
    break; 
  }
}